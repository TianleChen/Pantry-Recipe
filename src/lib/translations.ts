export type Language = 'en' | 'zh';
export type TranslationKey = keyof typeof translations.en;

export function t(lang: Language, key: string): string {
  const keys = key.split('.');
  let obj: any = translations[lang];

  for (const k of keys) {
    obj = obj?.[k];
  }

  if (!obj) {
    obj = translations.en;
    for (const k of keys) {
      obj = obj?.[k];
    }
  }

  return obj || key;
}
